<div id="bio">
<section>
<p>Strength often comes from starting over. In the aftermath of a disruption, calamity, or tragedy, there’s a process of restoration. Southern California quintet Of Mice & Men refer to it as Restoring Force. Not only does that serve as the title for the group’s third full-length album for Rise Records and follow-up to 2011 breakout The Flood, but it also sums up their unbreakable spirit. Enduring lineup shifts and a constant uphill battle to be heard, the band—Austin Carlile [lead vocals], Alan Ashby [rhythm guitars], Phil Manansala [lead guitar], Aaron Pauley [bass, vocals], and Valentino “Tino” Arteaga [drums]—emerges stronger than ever.
“What happens after a disaster? What happens after The Flood?” asks Carlile. “You return to normal. You find balance again. That’s what we felt like we were doing with this album. We officially added Aaron, and this is exactly where we needed to be with our sound. We wanted this record to bring equilibrium back to our band and music. We’re letting everyone know that we’re here to stay. We’re Restoring Force.”</p>
</section>
<section>
<p>Embracing that mindset, they headed to House of Loud Studios in New Jersey to record with producer David Bendeth [Paramore, Breaking Benjamin] at the start of June 2013. Through intense recording sessions, they challenged themselves and expanded their signature style.  As a result, they penned pummeling, passionate, and powerful anthems that teeter between searing screams and magnetic melodies. </p>
</section>
<section>
<p>“David is a character,” smiles Carlile. “He brought out the best in us by pushing us to the absolute limit. That’s why we wanted to work with him from the beginning. He worked us, shaped us, and molded us. We loved him sometimes, and we hated him others. He helped us reach a new level though.”
Part of the group’s ascent involved the incorporation of Pauley into every aspect of the creative process. His soaring hooks proved the perfect counterpoint to Carlile’s crushing screams. </p>
</section>
<section>
<p>“It was the first time we had every actually recorded together,” Carlile goes on. “He’s our brother, and he’s an incredible vocalist. I remember one night we were doing pre-production early on, and Aaron had some reservations. He was down on himself, and he didn’t know if he was cut out for it. I told him, ‘You’re the person who delivers that final punch and emotion. You’re the one who sends it home for us. Trust yourself’. The next day, he showed me ‘Feels Like Forever’.”
The track builds from a polyrhythmic riff into an utterly unforgettable refrain. It’s artfully heavy and chaotically catchy. </p>
</section>
<section>
<p>“I saw Aaron’s struggle in the studio, and I had the same struggle two weeks later,” admits Carlile. “I broke down. I was mentally, physically, and spiritually exhausted. That song takes me back to making this record. When we finished it, I was so reassured. You can always come out of any low point.”
Elsewhere on the record, “Bones Exposed” snaps from staggering guitars into a percussive onslaught led by the frontman’s inimitable growl and cinematic lyrics.
“This one’s heavier,” he affirms. “I was bitter, angry, hurt, and wounded. It’s about somebody you really don’t like at the moment. At the same time, we’re all broken. We’re all messed up. We’re all liars. We’re all sinners. We’re all flawed. None of us are perfect so you have to let it go at some point. It’s like a cut. If you leave it alone, it heals. Otherwise, you scratch it until the bones are exposed.”</p>
</section>
<section>
<p>Meanwhile, “Would You Still Be There?” merges the melodic and metallic seamlessly, and “You’re Not Alone” seesaws between an uplifting refrain and guttural verse. The latter touches on a larger theme for Carlile.
“The theme of the record is togetherness, working together, and being a family,” he reveals. “I want our listeners to know they’re not alone. We’re all going through it with them.”
The fans won’t take his words lightly. The group has become a veritable phenomenon worldwide, selling out shows across North America all the way to Australia, Europe, and beyond. The Flood reached #28 on the Billboard Top 200 and garnered praise from the New York Times, Rock Sound, Alternative Press, and more. With sales surpassing 125,000 and YouTube streams over 16,000,000, The Flood remains one of the most successful releases in 22 years of Rise Records.</p>
</section>
<section>
<p>In the end though, Restoring Force is a pivotal moment for Of Mice & Men that sees them gain power like never before. 
“Of Mice & Men has outgrown all of us,” concludes Carlile. “It’s bigger than the individuals comprising it. It’s bigger than the album. It has become its own thing. It’s more than just a rock group. I want this album to get people thinking and talking. It’s different. There are no cryptic messages. This is purely meant to move you.”
Force has effectively been restored for Of Mice & Men, and they’re ready to conquer.</p>
</section>
